bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        idle_start_time = timer_read();
        is_idle = false;
    }
    return true;
}

bool oled_task_user(void) {
    if (show_logo) {
        if (logo_start_time == 0) {
            logo_start_time = timer_read();
        }
        if (timer_elapsed(logo_start_time) < 3000) {
            oled_write_raw_P(idkpad_logo, sizeof(idkpad_logo));
            return false;
        } else {
            show_logo = false;
            oled_clear();
        }
    }

    if (timer_elapsed(idle_start_time) > 30000) {
        is_idle = true;
    }

	if (is_idle) {
        if (timer_elapsed(last_frame_time) > 50) {  // 50ms per frame = 20 FPS
            last_frame_time = timer_read();
            anim_frame = (anim_frame + 1) % 4;
        }
        oled_write_raw_P(anim_bitmap_allArray[anim_frame], sizeof(anim_frame));
        return false;
    }

    oled_write_raw_P(epd_bitmap_allArray[active_layer], frame_size[active_layer]);
    return false;
}

